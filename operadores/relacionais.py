x = 7
y = 5

print(x > y)        # Operador maior que
print(x >= y)       # Operador maior ou igual que
print(x < y)        # Operador menor que
print(x >= y)       # Operador menor ou igual que
print(x == y)       # Operador de igualdade
print(x != y)       # Operador de desigualdade

print('5' != 5)
print('5' == 5)

