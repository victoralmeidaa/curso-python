x = 3
y = 4

print(not False)    # imprime o valor de negacao de falso 
print(not True)     # imprime o valor de negacao de verdadeiro
print(-x)           # imprime o valor de negacao de x (inverte valor)
print(--y)          # imprime o valor de negacao da negacao de y
print(+3)           # nao existe operador de incremento e decremento